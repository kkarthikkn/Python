class Calc:
    @staticmethod
    def add(x,y):
        print(x+y)

    @staticmethod
    def sub(x, y):
        print(x - y)

    @staticmethod
    def mul(x, y):
        print(x * y)

    @staticmethod
    def div(x, y):
        print(x / y)


Calc.add(2,5)
Calc.sub(3,5)
Calc.mul(10,5)
Calc.div(8,5)